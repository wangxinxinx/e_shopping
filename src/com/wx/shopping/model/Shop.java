package com.wx.shopping.model;

public class Shop {
	private String sid;
	private String s_name;
	private String s_desc;
	private String s_pic_path;
	private double s_credit;
	private String s_reg_day;
	private String uid;
	public String getSid() {
		return sid;
	}
	public void setSid(String sid) {
		this.sid = sid;
	}
	public String getS_name() {
		return s_name;
	}
	public void setS_name(String s_name) {
		this.s_name = s_name;
	}
	public String getS_desc() {
		return s_desc;
	}
	public void setS_desc(String s_desc) {
		this.s_desc = s_desc;
	}
	public String getS_pic_path() {
		return s_pic_path;
	}
	public void setS_pic_path(String s_pic_path) {
		this.s_pic_path = s_pic_path;
	}
	public double getS_credit() {
		return s_credit;
	}
	public void setS_credit(double s_credit) {
		this.s_credit = s_credit;
	}
	public String getS_reg_day() {
		return s_reg_day;
	}
	public void setS_reg_day(String s_reg_day) {
		this.s_reg_day = s_reg_day;
	}
	public String getUid() {
		return uid;
	}
	public void setUid(String uid) {
		this.uid = uid;
	}
	

}
